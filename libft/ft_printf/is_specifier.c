#include "printf.h"

int	is_specifier(char c)
{
	if (c == 'd' || c == 'i' || c == 'u' || c == 'o' || c == 'x' || c == 'X')
		return (1);
	if (c == 'f' || c == 'F' || c == 'g' || c == 'G' || c == 'e' || c == 'E')
		return (1);
	if (c == 'a' || c == 'A' || c == 'c' || c == 's' || c == 'p' || c == 'n')
		return (1);
	if (c == '%')
		return (1);
	return (0);
}
